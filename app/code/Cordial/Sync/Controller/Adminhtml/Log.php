namespace Cordial\Sync\Controller\Adminhtml;

abstract class Log extends \Magento\Backend\App\Action
{

    const ADMIN_RESOURCE = 'Cordial_Sync::top_level';

    /**
     * @var \Magento\Framework\Registry
     */
    protected $coreRegistry;

    /**
     * @param \Magento\Backend\App\Action\Context $context
     * @param \Magento\Framework\Registry $coreRegistry
     */
    public function __construct(
        \Magento\Backend\App\Action\Context $context,
        \Magento\Framework\Registry $coreRegistry
    ) {
    
        $this->coreRegistry = $coreRegistry;
        parent::__construct($context);
    }

    /**
     * Init page
     *
     * @param \Magento\Backend\Model\View\Result\Page $resultPage
     */
    public function initPage($resultPage)
    {
        $resultPage->setActiveMenu(self::ADMIN_RESOURCE)
            ->addBreadcrumb(__('Cordial'), __('Cordial'))
            ->addBreadcrumb(__('Log'), __('Log'));
        return $resultPage;
    }
}
